/**
 * 按分类获取知识库
 */
export const getKnowledgeByCategory = (category: string): KnowledgeItem[] => {
  if (category === 'all') {
    return knowledgeData;
  }
  return knowledgeData.filter(item => item.category === category);
};

/**
 * 搜索知识库
 */
export const searchKnowledge = (keyword: string): KnowledgeItem[] => {
  const lowerKeyword = keyword.toLowerCase();
  return knowledgeData.filter(item =>
    item.title.toLowerCase().includes(lowerKeyword) ||
    item.content.toLowerCase().includes(lowerKeyword) ||
    item.tags.some(tag => tag.toLowerCase().includes(lowerKeyword))
  );
};

/**
 * 获取分类列表
 */
export const getCategories = (): { id: string; name: string; count: number }[] => {
  const categories = [
    { id: 'all', name: '全部', count: knowledgeData.length },
    { id: 'liuyao', name: '六爻', count: 0 },
    { id: 'bazi', name: '八字', count: 0 },
    { id: 'qimen', name: '奇门', count: 0 },
    { id: 'general', name: '通用', count: 0 },
  ];
  
  knowledgeData.forEach(item => {
    const cat = categories.find(c => c.id === item.category);
    if (cat) cat.count++;
  });
  
  return categories;
};
